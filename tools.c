#include "includes/fdf.h"

void	reset(t_data *x)
{
	x->y = 0;
	x->x = 0;
	x->p = 1;
	x->m = 1;
}

float	abs_val(float val)
{
	if (val < 0)
		return (val *= -1);
	return (val);
}
